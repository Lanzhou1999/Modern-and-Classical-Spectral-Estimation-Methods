%--------------------------------------------------------------
% 作业一,古典功率谱估计：使用FFT估计信号频谱
% 功能：打印出特定信噪比下的幅度谱
%--------------------------------------------------------------

n=0:255; 
xn=exp(1i*(2*pi*0.1*n+pi/3))+10*exp(1i*(2*pi*0.01*n+pi/4)); 
xn_=awgn(xn,30,'measured');  

xk=abs(fft(xn_,256));   %对随机信号xn_做快速傅里叶变换，并取幅值  

subplot(1,1,1); plot((0:255)/256,xk);
title('使用FFT估计复函数幅度谱(N = 256）');xlabel('f/2pi');